package com.Beans;

public class Ticket 
{
protected int ticketNo;
protected float price;
public int getTicketNo() {
	return ticketNo;
}
public void setTicketNo(int ticketNo) 
{
	this.ticketNo = ticketNo;
}
public float getPrice() 
{
	return price;
}
public void setPrice(float price) 
{
	this.price = price;
}

}
